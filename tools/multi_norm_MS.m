function [b1p, pval] = multi_norm_MS(X, c)
% =================================================================
% This function calculates Mardia's multivariate skewness
%
% syntax : [b1p, pval] = multi_norm_MS(X,c)
% inputs :
%   X: multivariate data, n x p matrix, n:sample size, p:dimensionality.
%   c: covariance normalized by N (c=1) or N-1 (c~=1)
%
% outputs :
%   b1p: test statistic
%   pval: p-value. for sample szie in [10 200] and dimension in [2 10], 
%   the p-value is computed by interpolation from the attached empirical critical values.
%
%
% created by    Chun-Chao Wang
%               Dept. of Statistics    
%               National Taipei University
%               Taipei, Taiwan
%
% References:
% Mardias KV (1970). "Measures of multivariate skewness and kurtosis with applications." 		
%           Biometrika, 57, 519-530. 
% Mardias KV (1974). "Applications of some measures of multivariate skewness and kurtosis in tesing normality and robustness studies. "
%           Sankhya: The indian Journal of Statistics, Series B. vol. 	36, no. 2, 	115-128.
%
% =========================================================

[n,p] = size(X);
dX = (X - repmat(mean(X),n,1));% (x_i - x_bar) for all i
if c == 1  % normalized by n
    S = cov(X,1);
else   % normalized by n-1
    S = cov(X);
end

G = dX*(S\dX');  
b1p = sum(sum(G.^3))/n^2;  % test statistic
K = ((p+1)*(n+1)*(n+3))/(n*(((n+1)*(p+1))-6));  % correction for n<=50
v = p*(p+1)*(p+2)/6; 

if n>50
    g = (n*b1p)/6;  %  ~ chi2(v)
else
    g = (n*b1p*K)/6;  % for small sample
end
% For sample size in [10 200] and deminsion in [2 10], 
% compute p-value by interpolating the empirical critical tables. 
% Otherwise, from the asymptotical chi2 distribution.
if (n<=200 && n>=10) && (p >=2 && p <=10) 
        pval=CVTable(b1p, p, n);
else
    pval = 1 - chi2cdf(g, v);  % p-value, asymptotically.
end


