function [wmin_m pval] = multi_norm_percentile(X,m,q)
% =================================================================
% This function generates the empirical q*100 th percentile of the Wmin_m(q)
% statistic from a set of W-tests, which contains m statistics W(X*c_i),
% i=1,2,...,m. c_i are m uniformly scattered points in p-dimensional uint
% sphere.
% limit: the dimension of the input data is restricted to between 2 and 20
% because of the availability of the empirical critical values. The sample
% size is suggested to be bewteen [10 500]. 
%
% syntax : wmin_m=multi_norm_percentile(X,p,m,q)
%
% inputs :
%   X: multivariate data, n x p matrix, n:sample size, p:dimensionality.
%   m: how many uniformly scattered points, 10000 is suggested to be the
%   minimum.
%   q: the percentile e.g. q=0.05 for the 5th percentile. q can be a
%   vector.
%
% outputs :
%   wmin_m : the wmin_m(q) test statistic from m W-tests. 
%   pavl: p-value(s) computed by interpolation from the attached empirical critical values.
%
% created by    Chun-Chao Wang
%               Dept. of Statistics    
%               National Taipei University
%               Taipei, Taiwan
%
% References:
% Wang CC, Hwang YT (2010). "A new functional statistic for multivariate normality." 
%       Statistics and Computing, Statistics and Computing, 21(4), 501-509.
% =================================================================
%

[n,p]=size(X);
if  (p < 2 || p > 20 || n>500)
    error('Invalid data dimension.');
end
% Standardize input data X into a matrix with identity covariance matrix
if rank(cov(X)) < p % cov(X) is nonsingular                        
    Z=X;% ill-conditioned, no standardization
else
    Z=(chol(cov(X))'\X')';
end

%-- The Box-Muller method
%     mu=zeros(1,p);
%     Sigma=eye(p);
%     Y=mvnrnd(mu,Sigma,m);% m x p
    Y = randn(m,p);% Independent multivariate normal.
%     YL=sort((Y./repmat(sqrt(sum(Y.^2,2)),1,p))*Z',2); % linear combinations
    YL=(Y./repmat(sqrt(sum(Y.^2,2)),1,p))*Z'; % linear combinations, m x n
    % for the sake of insufficient memory,use loop.
    if n*m < 1e07
        R=W_stat_1992(YL');% (1 x m) W test stats   
    else
        R=zeros(1,m);
        for i=1:m
            R(i)=W_stat_1992(YL(i,:));
        end
    end
    Rr=sort(R);
    wmin_m=Rr(ceil(m*q));
    pval=get_pval(wmin_m, p, n);
    
   
    
function pval=get_pval(S, p, n)
% compute p-value from the empirical critical value table by statistic, dimension p and sample size n
% limit: 2<= p <= 10, 10 <= n <= 200.

alfa=[0.0010 0.0100 0.0250 0.0500 0.0750 0.1000 0.2000];
% when p=11,13,14,16,17,18,19, use interpolation in demension p for CV.
all_p=[2:10 12 15 20];
if isempty(find(all_p==p, 1))% use interpolation
    [ncv,na]=size(CVTable(2));
    CVtmp=zeros(length(all_p),na);
    CV=zeros(ncv, na);
    for i=1:ncv
        for j=1:length(all_p)            
            TMP=CVTable(all_p(j));
            CVtmp(j,:)=TMP(i,:);
        end
        CV(i,1)=CVtmp(1,1);
        CV(i,2:end)=pchip(all_p',CVtmp(:,2:end)',p)';
    end
else% use pre-calculated critical values
    CV=CVTable(p);
end
% pchip performs better than spline here
cvs = pchip(CV(:,1),CV(:,2:end)',n);% interpolate samples 
pp=pchip(alfa, cvs);
nq=length(S);% number of qunatiles requested.
pval=zeros(1,nq);
for i=1:nq
    if S(i) > cvs(end)
        pval(i)=alfa(end);
    elseif S(i) < cvs(1)
        pval(i)=alfa(1);
    else
        I = find(S(i) < cvs, 1, 'first');
        pval(i) = fzero(@(x) ppval(pp,x) - S(i),alfa([I-1 I]));% be cautious with fzero
    end
end

