function [Eta pval] = multi_norm_SR(X)
% =================================================================
% This function calculates the statistic proposed by Szekely and Rizzo
% (equation (8) p.83)
%
% limit: the dimension of the input data is restricted to be less than and
% eauql to 20 because of the availability of the empirical critical values. The sample
% size is suggested to be bewteen [10 500]. 
%
% syntax : Eta=multi_norm_SR(X)
%
% inputs :
%   X: multivariate data , n x p matrix, n:sample size, p:dimensionality.
%
% outputs :
%   Eta : the test statistic. 
%   pval: the p-value, computed by interpolation from the attached empirical critical values.
%
% created by    Chun-Chao Wang
%               Dept. of Statistics    
%               National Taipei University
%               Taipei, Taiwan
%
% References:
% Szekely G. J., Rizzo M. L. (2005). "A new test for multivariate normality." 
%       Journal of Multivariate Analysis, 93, p. 58-80
% =================================================================
[n,p]=size(X);
if ( p > 20 || n>500)
    error('Invalid data size. The dimension p <= 20, sample size n <= 500');
end

% need to cope with rank deficiency
C=cov(X);
rp=rank(C);
if rp < p
    [H,Lambda] = eig(C);
    [tmp,I]=sort(diag(Lambda));
    X=X*H(:,I(p-rp+1:end));% replace original data by a dimension-reduced one.
%     p=rp;
end

X0=X - repmat(mean(X),n,1);
% Y=(chol(cov(X0))'\X0')'/sqrt(n-1);
Y=(chol(cov(X0))'\X0')';% standardized
% upper_bound=20;
k=0:30;% 21 is the max for the accuracy of the MATLAB factorial.
c1=(-0.5).^k;
c2=gamma(1.5+k);
c3=factorial(k);
c4=2*(2*k+1).*(k+1);
c5=gamma(k+p/2+1);

a_norm=sqrt(sum(Y .* Y,2));
Q=0;
for i=1:n
    c7=a_norm(i).^(2*k+2);
    Q=Q+sum(c1.*c2.*c7./c3./c4./c5);
end
EaZ=sqrt(2)*gamma((p+1)/2)*(n/gamma(p/2)+Q/sqrt(pi));

% Exception handling:
% When the largest k is set to odd, Q might diverge because of large
% a_norm. In that case, we assume the data is far away from normal and set
% the statistic to a huge positive number.
if EaZ<0 
    EaZ=-EaZ;
end

y_norm=0;
for i=1:n-1
        T=repmat(Y(i,:),n-i,1)-Y(i+1:n,:);%(n-i) x p
        y_norm = y_norm + 2*sum(sqrt(sum(T .* T,2)));% (n-i) x 1
end

Eta=n*(2/n*EaZ- 2*gamma((p+1)/2)/gamma(p/2) -y_norm/n^2);
pval= get_pval(Eta, p, n);


function pval=get_pval(S, p, n)
% compute p-value from the empirical critical value table by statistic, dimension p and sample size n
% limit: 1<= p <= 20, 10 <= n <= 500.

alfa=[0.0010 0.0100 0.0250 0.0500 0.0750 0.1000 0.2000];
% when p=11,13,14,16,17,18,19, use interpolation in demension p for CV.
all_p=[1:10 12 15 20];
if isempty(find(all_p==p, 1))% use interpolation
    [ncv,na]=size(CVTable(1));
    CVtmp=zeros(length(all_p),na);
    CV=zeros(ncv, na);
    for i=1:ncv
        for j=1:length(all_p)            
            TMP=CVTable(all_p(j));
            CVtmp(j,:)=TMP(i,:);
        end
        CV(i,1)=CVtmp(1,1);
        CV(i,2:end)=pchip(all_p',CVtmp(:,2:end)',p)';
    end
else% use pre-calculated critical values
    CV=CVTable(p);
end

cvs = pchip(CV(:,1),CV(:,2:end)',n);% interpolate samples 
pp=pchip(alfa, cvs);
if S < cvs(end)
    pval=alfa(end);
elseif S > cvs(1)
    pval=alfa(1);
else
    i = find(S > cvs, 1, 'first');
    pval = fzero(@(x) ppval(pp,x)- S,alfa([i-1 i]));% be cautious with fzero
end

