function [Sw2, pval]= multi_norm_SW2(b1p,b2p,n,p)
% =================================================================
% This function calculates Mardia's multivariate omnibus statistic.
%
% syntax : [Sw2, pval]= multi_norm_SW2(b1p,b2p,n,p)
% inputs:
%   b1p: Mardia's multivariate skewness
%   b2p: Mardia's multivariate kurtosis
%   n : sample size
%   p : p-variate 
%
% outputs:
%   Sw2: test statistic
%   pval: p-value, for sample szie in [10 200] and dimension in [2 10], 
%   the p-value is computed by interpolation from the attached empirical critical values.
%
%
% created by    Chun-Chao Wang
%               Dept. of Statistics    
%               National Taipei University
%               Taipei, Taiwan
%
% References:
% Mardias KV, Foster K (1983). "Omnibus tests of multinormality based on skewness and kurtosis." 
%           Communications in Statistics, Part A-Theory and Methods vol. 12, 207-221. 
%
% =================================================================

f=p*(p+1)*(p+2)/6; % (2.8)
Wb1p = (9*nthroot(4/3*n*f^2*b1p,3)-18*f+4)/6/sqrt(2*f);%~N(0,1), (3.7)

f1=6+sqrt(n*8*p*(p+2)/(p+8)^2)*(sqrt(0.5*p*(p+2)*n)/(p+3) + sqrt(1+0.5*p*(p+2)*n/(p+8)^2));%(3.9)
Eb2p=(n-1)*p*(p+2)/(n+1);% (2.4)
Vb2p=8*p*(p+2)/n;
x=(b2p-Eb2p)/sqrt(Vb2p);
% Wilson-Hilferty approximation, ~N(0,1)
Wb2p=3*sqrt(f1/2)*((1-2/(9*f1)) - nthroot((1-2/f1)/(1 + x*sqrt(2/(f1-4))),3));%(3.10)
Sw2=Wb1p^2+Wb2p^2; % ~ chi2(2), (3.11)

% For sample size in [10 200] and deminsion in [2 10], 
% compute p-value by interpolating the empirical critical tables. 
% Otherwise, from the asymptotical chi2 distribution.
if (n<=200 && n>=10) && (p >=2 && p <=10) 
    pval=CVTable(Sw2, p, n);
else
    pval = 1 - chi2cdf(Sw2, 2);% p-value, asymptotically.
end


